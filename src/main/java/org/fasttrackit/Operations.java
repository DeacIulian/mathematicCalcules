package org.fasttrackit;


public class Operations {

    static double gat(double a, double b) {
        return a + b;
    }
    static double dec(double a, double b) {
        return a - b;
    }
    static double mul(double a, double b) {
        return a * b;
    }
    static double div(double a, double b) {
        return a / b;
    }

}
